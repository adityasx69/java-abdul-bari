package section_2;

public class DefiningVariables {
    static void main() {
        byte by = 5;
        short s = 200;
        int i = 50000;
        float f = 1.3f;
        double d = 3.54;
        char c = 'A';
        System.out.println(by);
        System.out.println(s);
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
    }
}
