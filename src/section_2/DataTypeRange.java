package section_2;

public class DataTypeRange {
    static void main() {
        System.out.println("Integer Max Value " + Integer.MAX_VALUE);
        System.out.println("Integer Min Value " + Integer.MIN_VALUE);
        System.out.println("Integer Bytes " + Integer.BYTES);
        System.out.println();

        System.out.println("Double Max Value " + Double.MAX_VALUE);
        System.out.println("Double Min Value " + Double.MIN_VALUE);
        System.out.println("Double Bytes " + Double.BYTES);
        System.out.println();

        System.out.println("Character Max Value " + Character.MAX_VALUE);
        System.out.println("Character Min Value " + Character.MIN_VALUE);
        System.out.println("Character Bytes " + Character.BYTES);
    }
}
