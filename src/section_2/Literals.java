package section_2;

public class Literals {
    static void main() {
        byte b1 = 10;
        byte b2 = 0b1010;
        byte b3 = 012;
        byte b4 = 0XA;

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);
        System.out.println();

        long l = 999_999_999L;
        System.out.println(l);
        System.out.println();
        float f = 232_322.32f;
        System.out.println(f);
    }
}
